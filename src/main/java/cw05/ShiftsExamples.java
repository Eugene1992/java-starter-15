public class ShiftsExamples {
    public static void main(String[] args) {
        System.out.println(48 << 4); // * 2^n

//        0011 0000
//     2<<
//        1100 0000

        System.out.println(48 >> 2); // / 2^n

//        0011 0000
//     2>>
//        0000 1100

    }
}
